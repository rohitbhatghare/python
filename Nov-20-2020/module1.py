def sum(a, b):
    print("The addition of", a, "and", b, "is")
    return a + b


def sub(a, b):
    print("The Subtraction of", a, "and", b, "is")
    return a - b


def mul(a, b):
    print("The Multiplication of", a, "and", b, "is")
    return a * b


def div(a, b):
    print("The Division of", a, "and", b, "is")
    return a / b
